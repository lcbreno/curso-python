# Variáveis são usadas para salvar algo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar
# números e underline _.
# O sinal de = é o operador de atribuição. Ele é usado para
# atribuir um valor a um nome (variável).
# Uso: nome_variavel = expressão
nome_completo = 'Nome Da Pessoa'
soma_dois_mais_dois = 2 + 2
int_um = bool('1')
print(int_um, type(int_um))
print(nome_completo, soma_dois_mais_dois) 

# Variáveis não são usadas para abreviar código
# Variáveis são usadas para fazer um código mais legível
# Variáveis são usadas para não repetir coisas que são usadas em mais de um lugar
# O Nome da variável tem que ser o mais objetivo, curto e entendível possível 

nome = 'Pessoa'
idade = 30
maior_de_idade = idade >= 18
print('Nome:', nome, 'Idade:', idade)
print('É maior?', maior_de_idade)